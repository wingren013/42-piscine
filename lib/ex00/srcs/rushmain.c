#include <rushfuncs.h>

int		main(int ac, char **arg)
{
	int		x;
	int		y;

	(void)ac;
	if (ac == 3)
	{
		x = ft_atoi(arg[1], 0, 1);
		y = ft_atoi(arg[2], 0, 1);
	}
	else
	{
		x = 0;
		y = 0;
	}
	rush(x, y);
	return (0);
}
