int		ft_str_is_alpha(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i] != 0)
	{
		c = str[i];
		if ((c >= 'A' && c <= 'Z') || (c <= 'z' && c >= 'c'))
			i++;
		else
			return (0);
	}
	return (1);
}
